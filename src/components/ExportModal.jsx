/* ─── 4. ZIP (sequential PNG downloads) ─────────────────────────────────── */
async function downloadZip(exportDays, previewCache, buildKey, setProgress) {
  for (let i = 0; i < exportDays.length; i++) {
    const entry = exportDays[i];
    setProgress(Math.round(((i + 1) / exportDays.length) * 100));
    const card = document.createElement("canvas");
    const previewKey = null; // skip preview for zip to keep it fast
    await drawCard(card, entry, previewKey);
    await new Promise((resolve) => {
      card.toBlob((blob) => {
        const url = URL.createObjectURL(blob);
        const a   = document.createElement("a");
        a.href     = url;
        a.download = `outfit-${entry.day.id}-${entry.day.date.replace(/\s/g, "-")}.png`;
        a.click();
        setTimeout(() => { URL.revokeObjectURL(url); resolve(); }, 300);
      }, "image/png");
    });
  }
  setProgress(null);
}

