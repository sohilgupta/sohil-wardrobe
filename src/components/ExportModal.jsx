/* ─── ExportModal ─────────────────────────────────────────────────────────────
   Trip outfit export in 4 formats:
     1. Text itinerary   — .txt download
     2. Visual PNG cards — Canvas-rendered .png download (one combined image)
     3. PDF              — Print-ready HTML in a new window (uses browser print)
     4. Image ZIP        — One PNG per day, sequentially downloaded

   Source of truth: outfitIds (Daily tab data) + wardrobe items.
   Read-only. Never modifies outfit or wardrobe data.
   ─────────────────────────────────────────────────────────────────────────── */

import { useState, useEffect, useMemo } from "react";
import { T, CAT_EMOJI, swatch } from "../theme";
import TRIP from "../data/trip";
import { loadImg, resolveOutfitItems } from "../utils/canvasUtils";

const SLOT_LABELS = {
  daytime:   "Daytime",
  evening:   "Evening",
  breakfast: "Breakfast / Lounge",
  sleepwear: "Sleepwear",
  flight:    "Flight",
  activity:  "Activity",
};
const LAYER_ORDER = ["base", "mid", "outer", "thermalBottom", "bottom", "shoes"];
const LAYER_LABELS = { base: "Top", mid: "Mid", outer: "Outer", thermalBottom: "Thermal", bottom: "Bottom", shoes: "Shoes" };

/* ─── Utility: resolve outfit slot IDs → keyed object { [layer]: item } ─────
   (used by buildExportDays + drawCard; resolveOutfitItems from canvasUtils
    returns an array — this version returns an object keyed by layer name)       */
function resolveSlot(slotIds, wardrobe) {
  if (!slotIds) return null;
  const hasReal = Object.values(slotIds).some((v) => v && v !== "REMOVED");
  if (!hasReal) return null;
  const result = {};
  for (const k of LAYER_ORDER) {
    const id = slotIds[k];
    if (!id || id === "REMOVED") continue;
    const item = wardrobe.find((i) => i.id === id);
    if (item) result[k] = item;
  }
  return Object.keys(result).length > 0 ? result : null;
}

/* ─── Build list of days to export ──────────────────────────────────────── */
export function buildExportDays(outfitIds, frozenDays, wardrobe, frozenOnly) {
  return TRIP
    .filter((day) => {
      const data = outfitIds[day.id];
      if (!data) return false;
      const hasAny = Object.values(data).some((slot) =>
        slot && typeof slot === "object" && Object.values(slot).some((v) => v && v !== "REMOVED")
      );
      if (!hasAny) return false;
      if (frozenOnly && !frozenDays[day.id]) return false;
      return true;
    })
    .map((day) => {
      const data = outfitIds[day.id] || {};
      const slots = {};
      for (const slotKey of Object.keys(SLOT_LABELS)) {
        const resolved = resolveSlot(data[slotKey], wardrobe);
        if (resolved) slots[slotKey] = resolved;
      }
      return { day, slots };
    })
    .filter(({ slots }) => Object.keys(slots).length > 0);
}

/* ─── 1. TEXT EXPORT ─────────────────────────────────────────────────────── */
export function generateText(exportDays) {
  const lines = [
    "AUS & NZ 2026 — TRIP OUTFIT ITINERARY",
    "Generated by SOHIL-WARDROBE",
    "=".repeat(42),
    "",
  ];

  for (const { day, slots } of exportDays) {
    lines.push(`${day.date.toUpperCase()}  ·  ${day.city.toUpperCase()}`);
    lines.push(`Weather: ${day.w}  ·  ${day.occ}`);
    lines.push("");

    for (const [slotKey, items] of Object.entries(slots)) {
      lines.push(`  ${SLOT_LABELS[slotKey].toUpperCase()}`);
      for (const [layer, item] of Object.entries(items)) {
        lines.push(`    ${LAYER_LABELS[layer] || layer}: ${item.n}${item.b ? ` (${item.b})` : ""} — ${item.col}`);
      }
      lines.push("");
    }
    lines.push("-".repeat(42));
    lines.push("");
  }

  return lines.join("\n");
}

function downloadText(exportDays) {
  const text = generateText(exportDays);
  const blob = new Blob([text], { type: "text/plain" });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement("a");
  a.href     = url;
  a.download = "trip-outfits.txt";
  a.click();
  URL.revokeObjectURL(url);
}

/* ─── 2. CANVAS PNG EXPORT ───────────────────────────────────────────────── */
const CARD_W  = 380;
const CARD_H  = 500;
const THUMB_W = 80;
const THUMB_H = 80;
const GAP     = 10;
const PAD     = 18;

function drawColorSwatch(ctx, x, y, w, h, color, emoji) {
  const [bg, accent] = swatch(color || "Grey");
  const grad = ctx.createLinearGradient(x, y, x + w, y + h);
  grad.addColorStop(0, bg);
  grad.addColorStop(1, accent);
  ctx.fillStyle = grad;
  ctx.beginPath();
  ctx.roundRect(x, y, w, h, 10);
  ctx.fill();
  // emoji
  ctx.font = `${Math.floor(w * 0.4)}px serif`;
  ctx.textAlign = "center";
  ctx.textBaseline = "middle";
  ctx.fillText(emoji, x + w / 2, y + h / 2);
}

async function drawCard(canvas, dayEntry, previewUrl) {
  const { day, slots } = dayEntry;
  const ctx = canvas.getContext("2d");
  canvas.width  = CARD_W;

  // Calculate height based on number of items across all slots
  let itemCount = 0;
  for (const items of Object.values(slots)) itemCount += Object.keys(items).length;
  const rows   = Math.ceil(itemCount / 4);
  const height = 140 + rows * (THUMB_H + 36) + 40;
  canvas.height = Math.max(CARD_H, height);

  // Pre-load all images in parallel before drawing
  const allItems = Object.values(slots).flatMap((items) => Object.values(items));
  const imgResults = await Promise.all(allItems.map((item) => loadImg(item.img)));
  const imgMap = {};
  allItems.forEach((item, i) => { imgMap[item.id] = imgResults[i]; });
  const previewImg = previewUrl ? await loadImg(previewUrl) : null;

  // Background
  ctx.fillStyle = "#0F0F12";
  ctx.fillRect(0, 0, CARD_W, canvas.height);

  // Header bar
  ctx.fillStyle = "#1A1A1F";
  ctx.beginPath();
  ctx.roundRect(PAD, PAD, CARD_W - PAD * 2, 90, 14);
  ctx.fill();

  // Day emoji + city
  ctx.font = "bold 22px serif";
  ctx.textAlign = "left";
  ctx.textBaseline = "top";
  ctx.fillText(day.e, PAD + 14, PAD + 12);

  ctx.font = "700 15px 'Cormorant Garamond', Georgia, serif";
  ctx.fillStyle = "#E8E6E1";
  ctx.fillText(day.city.split("→")[0].trim(), PAD + 44, PAD + 12);

  ctx.font = "500 11px DM Sans, sans-serif";
  ctx.fillStyle = "#8A8780";
  ctx.fillText(day.date, PAD + 44, PAD + 32);

  ctx.font = "600 10px DM Sans, sans-serif";
  ctx.fillStyle = "#5C5A55";
  ctx.fillText(`${day.w.toUpperCase()}  ·  ${day.occ.toUpperCase()}`, PAD + 44, PAD + 52);

  if (day.day) {
    ctx.font = "400 10px DM Sans, sans-serif";
    ctx.fillStyle = "#8A8780";
    ctx.fillText(day.day.slice(0, 44), PAD + 14, PAD + 70);
  }

  // If AI preview available, draw it as background strip
  let yOffset = PAD + 104;

  if (previewImg) {
    {
      const ph = 180;
      ctx.save();
      ctx.beginPath();
      ctx.roundRect(PAD, yOffset - 6, CARD_W - PAD * 2, ph, 12);
      ctx.clip();
      ctx.drawImage(previewImg, PAD, yOffset - 6, CARD_W - PAD * 2, ph);
      ctx.restore();
      // gradient overlay
      const overlayGrad = ctx.createLinearGradient(0, yOffset - 6, 0, yOffset - 6 + ph);
      overlayGrad.addColorStop(0, "rgba(15,15,18,0)");
      overlayGrad.addColorStop(1, "rgba(15,15,18,0.7)");
      ctx.fillStyle = overlayGrad;
      ctx.fillRect(PAD, yOffset - 6, CARD_W - PAD * 2, ph);
      yOffset += ph + 8;
    }
  }

  // Outfit items by slot
  for (const [slotKey, items] of Object.entries(slots)) {
    // Slot label
    ctx.font = "700 9px DM Sans, sans-serif";
    ctx.fillStyle = "#5C5A55";
    ctx.textAlign = "left";
    ctx.textBaseline = "top";
    ctx.letterSpacing = "2px";
    ctx.fillText(SLOT_LABELS[slotKey].toUpperCase(), PAD, yOffset);
    yOffset += 16;

    // Items row
    const itemArr = Object.entries(items);
    let xOff = PAD;
    for (const [layer, item] of itemArr) {
      const img = imgMap[item.id];
      if (img) {
        ctx.save();
        ctx.beginPath();
        ctx.roundRect(xOff, yOffset, THUMB_W, THUMB_H, 10);
        ctx.clip();
        ctx.drawImage(img, xOff, yOffset, THUMB_W, THUMB_H);
        ctx.restore();
      } else {
        drawColorSwatch(ctx, xOff, yOffset, THUMB_W, THUMB_H, item.col, CAT_EMOJI[item.c] || "👕");
      }

      // Layer badge
      ctx.font = "700 7px DM Sans, sans-serif";
      ctx.fillStyle = "rgba(232,230,225,0.5)";
      ctx.textAlign = "center";
      ctx.fillText((LAYER_LABELS[layer] || layer).toUpperCase(), xOff + THUMB_W / 2, yOffset + THUMB_H + 4);

      // Item name
      ctx.font = "500 8px DM Sans, sans-serif";
      ctx.fillStyle = "#8A8780";
      const name = item.n.length > 10 ? item.n.slice(0, 9) + "…" : item.n;
      ctx.fillText(name, xOff + THUMB_W / 2, yOffset + THUMB_H + 15);

      xOff += THUMB_W + GAP;
      if (xOff + THUMB_W > CARD_W - PAD) { xOff = PAD; yOffset += THUMB_H + 28; }
    }
    if (xOff > PAD) yOffset += THUMB_H + 28;
    yOffset += 12;
  }

  // Footer watermark
  ctx.font = "600 8px DM Sans, sans-serif";
  ctx.fillStyle = "#26262E";
  ctx.textAlign = "center";
  ctx.fillText("SOHIL-WARDROBE · AUS & NZ 2026", CARD_W / 2, canvas.height - 12);
}

async function downloadVisualPNG(exportDays, previewCache, buildKey) {
  const totalH = exportDays.length * (CARD_H + 16) + 32;
  const master = document.createElement("canvas");
  master.width  = CARD_W + 32;
  master.height = totalH;
  const mCtx = master.getContext("2d");
  mCtx.fillStyle = "#080808";
  mCtx.fillRect(0, 0, master.width, master.height);

  let y = 16;
  // Slot order to check for a hero preview image — daytime first, then others
  const PREVIEW_SLOT_ORDER = ["daytime", "evening", "breakfast", "flight", "activity"];
  for (const entry of exportDays) {
    const card = document.createElement("canvas");
    // Use the first available preview across all slots (not just daytime)
    let previewUrl = null;
    if (buildKey && previewCache) {
      for (const slot of PREVIEW_SLOT_ORDER) {
        const slotItems = entry.slots[slot];
        if (!slotItems) continue;
        const slotIds = Object.fromEntries(
          Object.entries(slotItems).map(([k, item]) => [k, item.id])
        );
        const key = buildKey(entry.day.id, slot, slotIds);
        if (previewCache[key]) { previewUrl = previewCache[key]?.url || previewCache[key]; break; }
      }
    }
    await drawCard(card, entry, previewUrl);
    mCtx.drawImage(card, 16, y);
    y += card.height + 16;
  }

  master.toBlob((blob) => {
    if (!blob) { alert("Export failed: canvas rendering error. Try with fewer days selected."); return; }
    const url = URL.createObjectURL(blob);
    const a   = document.createElement("a");
    a.href     = url;
    a.download = "trip-outfits.png";
    a.click();
    URL.revokeObjectURL(url);
  }, "image/png");
}

